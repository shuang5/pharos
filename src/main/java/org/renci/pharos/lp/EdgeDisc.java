package org.renci.pharos.lp;

class EdgeDisc{
    private String id;
    private int value;
    public boolean canDoConversion=false;
    public EdgeDisc(String s1, int val) {
            id=s1;
            value=val;
    }
    
    @Override
    public String toString() {
            return "EdgeDisc [id=" + id + ", value=" + value + "]";
    }

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}
}